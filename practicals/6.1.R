# WAP in R to create a list containing strings, numbers, vector & a logical.
# Create a list named "list_data"
list_data <- list()

# Add elements to the list
# "Red" is a string
list_data[[1]] <- "shivam"

# "Green" is a string
list_data[[2]] <- "pankaj"

# c(21,32,11) is a vector of numbers
list_data[[3]] <- c(048,31,06)

# TRUE is a logical value
list_data[[4]] <- TRUE

# 51.23 is a number
list_data[[5]] <- 51.23

# 119.1 is a number
list_data[[6]] <- 119.1

# Display the contents of the list
print(list_data)