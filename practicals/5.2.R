# Write a program in R to add , multiply , divide, two vector of integer type of length 3

# Create two vectors of integers
x <- c(10, 20, 30)
y <- c(20, 10, 40)

# Add the vectors
add_vec <- x + y
print("After adding vectors:")
print(add_vec)

# Multiply the vectors
mul_vec <- x * y
print("After multiplying vectors:")
print(mul_vec)

# Divide the vectors
div_vec <- x %/% y
print("After dividing vectors:")
print(div_vec)